	<article class="w3-container" style="width:auto; height:auto; margin-right:20px">
		<h1>INANI BEACH</h1>
		<p style="text-align:center"><img src="inani1.jpg"></p>
		<p>Inani Beach is within Ukhia Thana, 35 km. to the south of Cox's Bazar.<br> 
		With green hills to the east, the golden beach of Inani casts a music spell on anyone stepping on to its fine golden sands. The clean blue waters of the Bay are ideal for swimming.<br> 
		It is considered the world's longest and broadest beach: 180m at high tide and 300m at low tide. </p>
		<h5><strong>HOW TO GO</strong></h5>
		<p>To get here, take a bus to Teknaf and get off at Court Bazar (30km), a tiny village 2km before Ukhia. From there, you can rickshaw or tempo west to the beach, 10 km away. If you're headed for the guesthouse, ask the rickshaw driver to let you off at the tiny village of Sonarpara. From there, walk south until you reach the guesthouse. Alternatively, hire a jeep from Cox's Bazar to take you directly to Inani using the scenic beach route.</p>
		<p style="text-align:center"><img src="inani3.jpg"></p>
		<h5><strong>ATTRACTIONS</strong></h5>
		<p>
			1.Backdrop of lush green hills rising on the east.<br>
			2.The sea stretching out endless to the west.</br>
			3.Unusual shaped rock and coral boulders scattered lavishly in the beach and through the sea.<br>
			4.Fringed with tall palm trees swaying gently in the breeze .<br>
			5.Seashells of different colours , shapes , and sizes are found along here .<br>
			6.Calm lagoon which is wonderful for the little ones to paddle in .<br>
			7.Getting on the rock & coral boulders with the waves washing up on the shore around you, will be an extremely therapeutic experience.<br>
			8.Shark free water is suitable for sea bathing.<br>
		</p>
		<h5><strong>NOTE</strong></h5>
		<p>Every year numerous foreign and local tourists come here to spend their leisure in Inani Beach. However, it lacks several amenities that visitors can expect. It is wise to take a packed lunch and water with you when you visit the beach. But those people who have taken the time to explore the beach can testify that its beauty and cool breeze far outweighs anything it might be lacking in amenities. When visiting Inani Beach, ensure you have camera along to capture the amazing sight of the sun setting over the sea.</p>
