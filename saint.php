	<h1>SAINT MARTIN</h1>
	<p style="text-align:center"><img src="saint3.jpg"></p>
	<p>St. Martins Island is the most beautiful Coral Island where you will find live corals. Its only 30 km from Teknaf and you can go there by local motorboat, tourist boats, or sea truck.This small coral island about 10km (6mi) southwest of the southern tip of the mainland is a tropical cliche, with beaches fringed with coconut palms and bountiful marine life. There's nothing more strenuous to do here than soak up the rays, but it's a clean and peaceful place without even a mosquito to disrupt your serenity.</p>
	<h5><strong>TOURIST SEASON</strong></h5>
	<p>November to February</p>
	<h5><strong>Original Name</strong></h5>
	<p> "NarikelJinjira" (Coconut Island) - The local name of the COCONUT is NARIKEL and the original name of this Island is NarikelJinjira is derived from that.</p>
	<p style="text-align:center"><img src="saint4.jpg"></p>
	<p>
		<strong>Features: </strong><br>
1) Beautiful Coral Reef <br>
2) Long Coral Beach <br>
3) Lots of Coconut Trees <br>
4) Peaceful Environment <br>
5) Bountiful Marine Life <br>
6) Deep Blue Water <br>
7) Sandy Beach<br>
<br>

<strong>Sunrise & Sunset: </strong>Observing sunrise and sunset St Martins is a better place. Sunset Point is a rocky peninsula on the northwest corner of the island. <br>
<br>
<strong>Village life: </strong> A slow paced life - most work happens in the morning and evening, whereas midday is reserved for drying rice and fish. During the day time, some villagers sells coconuts while others sitting in the shade and chewing paan (battle leaf). <br>
<br>
<strong>Sea Turtle Hatchery: </strong> Located on the west side of the island south of ShemanaPereye Resort. The hatchery is not officially open to tourists; however, it is generally empty and the gate unlocked. <br>
<br>
<strong>Stars: </strong>Amazingly plentiful stars get on a moonless night, due to the lack of electricity on St Martine. <br>
<br>
<strong>Scuba Diving: </strong> Oceanic Scuba Dive Center, the only dive operation in Bangladesh. To experience the beauty underwater scuba diving is best but the cost is lit-bit high though negotiable. <br>
<br>
<strong>Embrace Moonlight: </strong> Make a tour in St Martins by yourself in the moonlight. The astonishing beauty of full-moon in St. Martin's Island never be expressed; you can only feel it. Over the night feel and listen the sound of the Bay of Bengal.<br> 
<br>
<strong>Watching Sun Rise: </strong> Wake up early and watch the sun rise. <br>
<br>
<strong>Campfire: </strong>Make a campfire to stay up at night. <br>
<br>
<strong>Shopping: </strong>A good number of small shops are near the port and along the main road selling essential commodities, odd souvenir t-shirts, hats and sunglasses. Various sea shells and things made from the shells are sold by teens around St Martine. However, it's illegal as leads to the depletion of sea life. <br>
<br>
<strong>Accommodations: </strong>Accommodations facilities are very good here. You can easily manage a room according to your budget. Most of the evenings can be candle-lit as no electricity on St Martins, but a few of the much expensive hotels have generators services.<br>
	</p>
